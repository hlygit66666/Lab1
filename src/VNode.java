
// 邻接表中表的顶点
public class VNode {
	String data = "*"; // 顶点信息
	ENode firstEdge; // 指向第一条依附该顶点的弧
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public ENode getFirstEdge() {
		return firstEdge;
	}
	public void setFirstEdge(ENode firstEdge) {
		this.firstEdge = firstEdge;
	}
	
}
